package fabrica;

public class Area {
	int division;
	int area;
	String descripcion;
	int capacidad;
	
	public Area(int division, int area, String descripcion, int capacidad) {
		super();
		this.division = division;
		this.area = area;
		this.descripcion = descripcion;
		this.capacidad = capacidad;
	}
	
	

}
